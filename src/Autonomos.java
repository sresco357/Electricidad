public class Autonomos {

    /**
     * Atributos, propiedades, Variables
     */
    private Integer codcli;
    private String nombre;
    private String apellido;
    private String dni;
    private String direcpos;
    private String pobla;
    private String provin;
    private String email;
    private Integer telefono;

    /**
     * Metodos, secciones, comportamiento,
     */
    public Integer getCodcli() {
        return codcli;
    }

    public void setCodcli(Integer codcli) {
        this.codcli = codcli;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getDirecpos() {
        return direcpos;
    }

    public void setDirecpos(String direcpos) {
        this.direcpos = direcpos;
    }

    public String getPobla() {
        return pobla;
    }
    public void setPobla(String pobla) {
        this.pobla = pobla;
    }

    public String getProvin() {
        return provin;
    }
    public void setProvin(String provin) {
        this.provin = provin;
    }
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    public Integer getTelefono() {
        return telefono;
    }
    public void setTelefono(Integer telefono) {
        this.telefono = telefono;
    }
}
